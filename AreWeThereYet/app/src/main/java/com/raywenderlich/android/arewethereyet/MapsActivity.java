    class FetchThingspeakTask extends AsyncTask<Void, Void, String> {



        protected void onPreExecute() {


        }

        protected String doInBackground(Void... urls) {
            try {
                URL url = new URL(THINGSPEAK_CHANNEL_URL + THINGSPEAK_CHANNEL_ID +
                        THINGSPEAK_FEEDS_LAST + THINGSPEAK_API_KEY_STRING + "=" +
                        THINGSPEAK_API_KEYR + "");
                HttpURLConnection urlConnection = (HttpURLConnection) url.openConnection();
                try {
                    BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(urlConnection.getInputStream()));
                    StringBuilder stringBuilder = new StringBuilder();
                    String line;
                    while ((line = bufferedReader.readLine()) != null) {
                        stringBuilder.append(line).append("\n");
                    }
                    bufferedReader.close();
                    return stringBuilder.toString();
                }
                finally{
                    urlConnection.disconnect();
                }
            }
            catch(Exception e) {
                Log.e("ERROR", e.getMessage(), e);
                return null;
            }
        }

        protected void onPostExecute(String response) {
            if(response == null) {
                Toast.makeText(MapsActivity.this, "There was an error", Toast.LENGTH_SHORT).show();
                return;
            }


            try {
                String add="l";
                JSONObject channel = (JSONObject) new JSONTokener(response).nextValue();
                double latitude = channel.getDouble(THINGSPEAK_FIELD1);
                double longitude = channel.getDouble(THINGSPEAK_FIELD2);
                baggage=new LatLng(latitude,longitude);
                Geocoder geocoder = new Geocoder(getApplicationContext(), Locale.getDefault());
                try {
                    List<Address> listAddresses = geocoder.getFromLocation(latitude, longitude, 1);
                    Address obj = listAddresses.get(0);
                    add = obj.getLocality();
                    add = add + "," + obj.getAdminArea();
                    add = add + "," + obj.getCountryName();
                } catch (IOException e) {
                    e.printStackTrace();
                }
                mMap.addMarker(new MarkerOptions().position(baggage).title(add));
                startService(new Intent(MapsActivity.this,AreWeThereIntentService.class));
            } catch (JSONException e) {
                e.printStackTrace();
            }
        }
    }
